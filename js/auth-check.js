function isAuthPage() {
    // –ü—Ä–æ–≤–µ—Ä—è–µ–º –ø–æ –∏–º–µ–Ω–∏ —Ñ–∞–π–ª–∞ –≤ URL, –ø—É—Ç–∏ –∏–ª–∏ –Ω–∞–ª–∏—á–∏—é —ç–ª–µ–º–µ–Ω—Ç–∞ —Ñ–æ—Ä–º—ã –≤—Ö–æ–¥–∞
    return window.location.pathname.endsWith('auth.html') || 
           window.location.pathname.includes('/auth.html') ||
           document.getElementById('login-form') !== null;
}

